const mongoose = require('mongoose');
require('dotenv').config();

const PREFIX = '?';
const client = new Client({ intents: [
  GatewayIntentBits.Guilds,
  GatewayIntentBits.GuildMessages,
  GatewayIntentBits.MessageContent
]});

